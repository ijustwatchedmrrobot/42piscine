#include <unistd.h>

char	*ft_strlowcase(char *str)
{
	int	down;
	int	i;

	down = 'A' - 'a';
	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= 'A' && str[i] <= 'Z')
		{
			str[i] -= down;
		}
		i++;
	}
	return (str);
}
