#include <unistd.h>

char	*ft_strupcase(char *str)
{
	int	up;
	int	i;

	up = 'A' - 'a';
	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= 'a' && str[i] <= 'z')
		{
			str[i] += up;
		}
		i++;
	}
	return (str);
}
