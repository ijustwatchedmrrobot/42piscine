#include <unistd.h>

void	ft_putchar(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		write (1, &str[i], 1);
		i++;
	}
}

int	main(int arg, char **args)
{
	if (arg > 0)
		ft_putchar(*args);
	write (1, "\n", 1);
}
